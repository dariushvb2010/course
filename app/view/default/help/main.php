<h2 class="helph" id="correspondence">مکاتبات</h2>
<h3  class="helph">ثبت فرآیند های پرونده </h3>

<p>

در این صفحه شما با وارد کردن شماره کوتاژ یا کلاسه می توانید لیست تمامی عملیات ها و فرایند های ممکن را برای آن پرونده مشاهده کنید.
برای اظهارنامه هایی که هنوز ثبت کلاسه نشده اند باید کوتاژ را وارد کنید  تا آن ها را ثبت کلاسه نمایید.
بعد از وارد کردن کلاسه یا شماره کوتاژ و زدن دکمه ورود لیستی از تمامی فرایند های ممکن با توجه به وضعیت پرونده به شما نشان داده می شود :
</p>
	<ul>
	<li>ثبت کلاسه</li>
	<li>ارسال مطالبه نامه</li>
	<li>ثبت ابلاغ مطالبه نامه</li>
	<li>اعتراض صاحب کالا</li>
	<li>ارسال رای دفاتر ستادی به صاحب کالا</li>
	<li>ارسال نظر کارشناس</li>
	<li>ارسال پرونده به کمیسیون</li>
	<li>ارسال به دفاتر ستادی</li>
	<li>ارسال به کمیسیون تجدید نظر</li>
	<li>رای کمیسون تجدید نظر به نفع صاحب کالا</li>
	<li>رای کمیسون تجدید نظر به نفع گمرک</li>
	<li>رای کمیسون به نفع صاحب کالا</li>
	<li>رای کمیسون به نفع گمرک</li>
	<li>رای دفاتر ستادی به نفع صاحب کالا</li>
	<li>رای دفاتر ستادی به نفع گمرک</li>
	<li>ثبت ابلاغ ثانویه</li>
	<li>ثبت ابلاغ رای دفاتر ستادی</li>
	<li>ثبت ابلاغ رای کمیسیون</li>
	<li>تحویل به کارشناس</li>
	<li>تسویه حساب</li>
	</ul>
<p>
به عنوان مثال در تصویر زیر کوتاژی وارد شده و بر روی دکمه ی ورود کلیک کردیم سیستم به طور خودکار شماره کلاسه آن را در جعبه متن کلاسه نوشته و تمامی فرایند های ممکن آن را در جعبه زیر آورده است.
از آن جایی که کوتاژی که وارد شده آخرین فرایندش ثبت کلاسه بوده است الان تنها فرایند ممکن برای آن کوتاژ ارسال مطالبه نامه می باشد :
</p>
<div align=center><img  src="/img/help/ex1.png" /></div>
<p>
	اگر پس از وارد کردن شماره کلاسه یا کوتاژ فرایند مورد نظر خود را مشاهده نکردید با مدیر سیستم تماس بگیرید
	.فرایند تحویل به کارشناس یک پرونده را به کارشناس قبلی و اگر آن کارشناس موجود نبود به کارشناسی دگر به صورت تصادفی اختصاص می دهد و بعد از این عملیات آن کارشناس این پرونده را در میز کار خود در قسمت کارشناسی پرونده مشاهده می کند.
	در فرایند اعتراض صاحب کالا یک لیست مرجع در خواستی وجود دارد که مشخص می کند که صاحب کالا چه تقاضایی دارد مثلا تقاضا می کند که پرونده را به کمیسیون حل اختلافات گمرکی بفرستید.
	بعد از انتخاب فرایند مورد نظر به صفحه ی ورود اطلاعات برای آن فرایند منتقل می شوید.
</p>
