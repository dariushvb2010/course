		<li>رویدادهای فعال کننده: وصول دفتر کوتاژ</li>
		<li>رویدادهای غیرفعال کننده: هیچ کدام</li>
		</ul>
	</p>
	<p>یا مثلا مدیر تصمیم دارد که یک اظهارنامه با کوتاژ 6053434 به سرعت کارشناسی شود. لذا یک یادآور برای بایگانی بازبینی تنظیم می کند تا این اظهارنامه سریعا به کارشناس تخصیص داده شود. تا زمانی که این اظهارنامه به کارشناس تخصیص داده نشود. این یادآور در میزکار بایگانی بازبینی خواهد ماند</p>
