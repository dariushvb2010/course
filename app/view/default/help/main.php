<p>
پس از انتخاب  یکی از پرونده هایی که به شما اختصاص داده شده است در صفحه ای به شکل زیر 


<div align=center><img  src="/img/help/ex3.png" /></div>
به صفحه ی کارشناسی پرونده منتقل می شوید 
این صفحه بسیار شبیه کارشناسی اظهارنامه است .
شما با انتخاب پرونده ای که به شما اختصاص داده شده است به صفحه ی کارشناسی پرونده می روید .
در آن صفحه شما باید یکی از گزینه های 
</p>
<ul>
<li>قبول اعتراض صاحب کالا</li>
<li> رد اعتراض</li>
