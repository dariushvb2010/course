<h3 class="helph">ثبت جزئیات فرایند </h3>
<p>
در این صفحه جزییات و اطلاعات برای یک فرایند مکاتبات وارد می شود و ساختن فرایند تکمیل می گردد .
</p>
<p>
ابتدا باید شماره اندیکاتور نامه ی اتوماسیون را وارد کنید که باید یک عدد باشد و تنها کارکتر های عددی مجاز می باشند در غیر این صورت با پیغام خطا مواجه می شودید .
</p>
<p>
سپس توضیحات در صورت لزوم و در اخر تصویر نامه ی اتوماسیون  را به دو روش می توانید بار گذاری کنید اول این که روی دکمه ی انتخاب عکس کلیک کنید و عکس خود را انتخاب کنید و
روش دوم این که عکس را کشیده و در صفحه اورده و در محل مشخص شده رها کنید.
</p>	
<p>
پس از ورود اطلاعات به صفحه ی  قبلی منتقل شده و نتیجه ی ثبت فرایند را مشاهده می کنید .
</p>	


